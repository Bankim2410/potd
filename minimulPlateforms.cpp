class Solution {
  public:
    // Function to find the minimum number of platforms required at the
    // railway station such that no train waits.
    int findPlatform(vector<int>& a, vector<int>& d) {
        sort(a.begin(), a.end());
    sort(d.begin(), d.end());
    int n = a.size(), p = 0, m = 0, i = 0, j = 0;
    while (i < n && j < n) {
        if (a[i] <= d[j]) p++, i++;
        else p--, j++;
        m = max(m, p);
    }
    return m;
    }
};


